NXP.addMobile = function(link) {
    if (link.indexOf('site_preference') != -1) {
        var end = link.indexOf('site_preference') - 1;
        link = link.substr(0, end);
    }
    var mobile_q = "?site_preference=mobile";
    var mobile_a = "&amp;site_preference=mobile";
    if (link == 'http://www.nxp.com/') {
        link = link+"m_index.html";
    }
    link = link.indexOf('?') === -1 ? link+mobile_q : link+mobile_a;
    return link;
}

