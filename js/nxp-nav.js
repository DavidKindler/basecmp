NXP.returnDate = function(dateString) {
    var temp = new Date(dateString);
    var dateStr = temp.getDate().toString()+" "+NXP.month[temp.getMonth()].toString()
                  +" "+temp.getFullYear().toString();
    return dateStr;
}
