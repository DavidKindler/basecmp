				<h3 class="tocable">Envelopes</h3>
				<br />

				<h3>Logo</h3>
				<p>The logo appears in the top left corner of the envelope. For visual consistency, use the same size logo for all envelope sizes and the letterhead. Always use the three color logo.</p>

				<h3>Color</h3>
				<p>On standard white envelopes, the logo is always printed in the company colors. Brown envelopes should be avoided as the colors of the logo will be reproduced poorly. Alternatively use white mailing labels carrying the logo in the company colors.</p>

				<h3>Printing</h3>
				<p>Do not include a postal address on window envelopes as the letterhead address is visible through the window. On envelopes without a window, position the postal return address as shown in the layout.</p>

				<h3>Paper</h3>
				<p>Choose a good quality white stock for standard white envelopes that matches the color of the letterhead as closely as possible.</p>

				</div>
				<div class="print-text-column">

					<h3>Franking</h3>
					<p>Do not incorporate the logo into franking plates. When printing on an envelope, ensure the franking impression does not cover the logo.</p>

					<h3>Sizes</h3>
					<p>Standard envelope sizes for A4 letterheads: C6/5: 114 x 224mm, C5: 162 x 229mm, C4: 229 x 324mm.	Standard envelope size for US letterheads: No. 10, 4 1/8 x 9 1/2 inches (105 x 241mm).</p>

					<h3>Address line</h3>
					<p>The NXP address underneath the logo should be on one line if possible, but if necessary it can be extended up to three lines.</p>

					<h3>Address line on window envelopes</h3>
					<p>If the address goes over two lines and there is a window on the envelope, place it in the bottom left corner, using the same margin as the logo above (50% of the logo’s height).</p>

					<h3>Logo position</h3>
					<p>In some countries, the stamp or frank is placed on the top left of the envelope. In this case, the logo should be moved to the bottom left corner, using the same margin as the logo above (50% of the logo’s height).</p>

