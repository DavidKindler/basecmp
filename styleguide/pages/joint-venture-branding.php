					<p>A NXP Joint Venture always requires a name, a logo and an identity. These have to be described in a corporate identity document. The depth of this document can vary.</p>
					<p>Alignment with the Corporate Communications and Branding department of NXP is required. Contact them at brand. support@nxp.com to get advice and discuss exceptions etc. Always refer to the legal department for advice regarding trademarks, trade names and domain names.</p>
					<p>The NXP and the partner brand identity should be respected in the creation of the joint venture brand. See http://www.nxp.com/smo/branding/ for more information on the NXP brand.</p>
					<h3>Process</h3>
					<p>The process to think about the joint branding issues starts during the joint ventures negotiations and should be clear before reaching a joint venture agreement. NXP Corporate Communications & Branding can assist in this process.</p>
					<p>It is advised to dedicate a chapter in the joint venture agreement to branding and naming.</p>
					<p>NXP Corporate Communications & Branding should be contacted before the start of the design process for guidance on the name, logo and identity of the joint venture brand.</p>

				</div>
				<div class="print-text-column">
					<p><br />Be aware that creating a new brand takes time, funding and dedication.</p>
					<h3>Branding & logos</h3>
					<p>When more than two parent brands are creating the joint venture then a new brand name is clearly preferred. Only seldom is it possible to combine three or more parent names in one new joint venture name.</p>
					<p>When more brands are stakeholders in a joint venture then their respective parent brand logos can be used in the communication to support the joint venture, see the next page for recommendations.</p>
					<p>During a certain period it is advised to use the parent brands in the communication to support the positioning of the joint venture.</p>
					<h3>Identity</h3>
					<p>The creation of the joint venture identity has to be done with respect for the basic principles behind the parent brand identity strategies.</p>
					<p>To avoid any confusion or dilution of the parent brands it is therefor NOT allowed to base the joint venture identity on identity elements (colors, fonts, logo, etc..) of the parent companies.</p>
